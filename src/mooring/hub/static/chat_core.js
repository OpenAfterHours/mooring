    COMMANDS,
    parseSlash,
    unescapeSlash,
    filterCommands,
    isSlashTyping,
    explainPrompt,
    explainLabel,
    reviewPrompt,
    reviewLabel,
    checksPrompt,
    checksLabel,
    sqlPrompt,
    sqlLabel,
    notesCellPrompt,
    HistoryRing,
    mentionMatch,
    filterDatasets,
    applyMention,
    additiveBlockLines,
    diffLines,
    piiBadge,
    tracebackHoldSummary,
    scanErrorMessage,
    parseDeviceLogin,
